
客户端：程序调用者
服务端：依据接口开发程序的人

概念：当我使用某个产品时，我需要看一下它的使用说明书。我依据说明书上的各项功能去使用这个产品
这里的说明书就相当于interface，我们可以在interface看到实现这个接口的各项功能
这里的“我”就相当于客户端，可以调用实现这个接口的程序以用到说明书的功能
而生产这个产品的人就相当于服务端，它需要依照说明书的内容开发功能

“面向接口”开发：就是客户端和服务器端都围绕interface进行开发

看interface时候的关注点，
如果你是客户端：你可以知道代码设计者把哪些功能模块拆分出来了，这样你就能知道这个类具有的功能
如果你是服务器：你可以知道接下来代码需要围绕哪些功能进行开发

